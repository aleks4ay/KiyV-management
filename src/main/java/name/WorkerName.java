package name;

public final class WorkerName {
    String workerName = "";

    public String getWorkerNames(String s) {
        if (s.equals("BONDARENKO-PC")) return "Бондаренко";
        if (s.equals("CHIGRIN-PC")) return "Чигрин";
        if (s.equals("GRIZUTA-PC")) return "Грицута";
        if (s.equals("GRIZUTA-PC")) return "Грицута";

        return s;
    }
}
